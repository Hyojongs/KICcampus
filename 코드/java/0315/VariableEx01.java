public class VariableEx01 {
	public static void main(String[] args) {
		// 변수의 선언
		int num1;
		// 변수의 초기화
		num1 = 10;
		// 변수의 사용
		System.out.println( num1 );

		// 선언과 동시에 초기화
		int num2 = 20;
		System.out.println( num2 );

		//int num1 = 20;

		int num3, num4 = 50;
		System.out.println( num4 );
		num3 = 0;
		System.out.println( num3 );
	}
}
